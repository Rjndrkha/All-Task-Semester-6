    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        customer cust = new customer();
        cust.setId_custom(Integer.parseInt(txtID.getText()));
        cust.setFirstname(txtFirst.getText());
        cust.setLastname(txtLast.getText());
        cust.setPhone(txtPhone.getText());
        cust.setGender(JlistGen.getSelectedValue().toString());
        cust.setAddress(txtAddress.getText());
        cust.getServis().setId_service(Integer.parseInt(txtIDservice.getText()));
        cust.save();
        txtID.setText(Integer.toString(cust.getId_custom()));
        tampilkanData();
    }//GEN-LAST:event_btnSaveActionPerformed

