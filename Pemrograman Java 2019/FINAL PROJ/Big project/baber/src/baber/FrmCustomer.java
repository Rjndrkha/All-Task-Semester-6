     public void tampilkanData(){
        String[] kolom = {"ID", "Firstname", "Lastname","Phone", "Gender", "Address", "id_service"};
        ArrayList<customer> list = new customer().getAll();
        Object rowData[] = new Object[7];
        
        tblCustom.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(customer cust : list){
            rowData[0] = cust.getId_custom();
            rowData[1] = cust.getFirstname();
            rowData[2] = cust.getLastname();
            rowData[3] = cust.getPhone();
            rowData[4] = cust.getGender();
            rowData[5] = cust.getAddress();
            rowData[6] = cust.getServis().getId_service();
   
            
            ((DefaultTableModel)tblCustom.getModel()).addRow(rowData);
        }
    }
     
     public void search(String keyword){
        String[] kolom = {"ID", "Firstname", "Lastname", "Phone", "Gender", "Address", "id_service"};
        ArrayList<customer> list = new customer().search(keyword);
        Object rowData[] = new Object[7];
        
        tblCustom.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(customer cust : list){
            rowData[0] = cust.getId_custom();
            rowData[1] = cust.getFirstname();
            rowData[2] = cust.getLastname();
            rowData[3] = cust.getPhone();
            rowData[4] = cust.getGender();
            rowData[5] = cust.getAddress();
            rowData[6] = cust.getServis().getId_service();
   
            
            ((DefaultTableModel)tblCustom.getModel()).addRow(rowData);
        }
    }
     
     public void tampilkanDataTabel(){
        String[] kolom = {"ID", "Service Name", "Duration","Price"};
        ArrayList<service> list = new service().getAll();
        Object rowData[] = new Object[4];
        
        tblService.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(service serv : list){
            rowData[0] = serv.getId_service();
            rowData[1] = serv.getName();
            rowData[2] = serv.getDuration();
            rowData[3] = serv.getPrice();

            ((DefaultTableModel)tblService.getModel()).addRow(rowData);
        }
    }
     
    

